//area of rectangle
#include <iostream>
using namespace std;
int main() {
    int length=3;
    int width=4;
    int area=length*width;
    cout << "the length of the rectangle is " << length << "\n";
    cout << "the width of the rectangle is " << width << "\n";
    cout << "the area of the rectangle is " << area << "\n";
    
    return 0;
}